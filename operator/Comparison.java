package operator;

public class Comparison {

	public static void main(String[] args) {
		int a = 45 ;
		int b = 64 ;
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a == b);
		System.out.println(a != b);
		
	}

}
